import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const sRoot = path.join(repoRoot, 's');
const outPath = path.join(sRoot, 'build/runtime-globals.js');

const constsSrc = readFileSync(path.join(sRoot, 'vue/consts.js'), 'utf8');
const names = [...constsSrc.matchAll(/var (constant_\w+) =/g)].map((m) => m[1]);
names.push('Instruments');

mkdirSync(path.dirname(outPath), { recursive: true });

const lines = [
  '// Auto-generated by scripts/generate-runtime-globals.mjs — do not edit.',
  '// Maps legacy window globals (from deferred classic scripts) for the Vite bundle.',
  ''
];

for (const name of names) {
  lines.push(`export const ${name} = globalThis.${name};`);
}

writeFileSync(outPath, lines.join('\n') + '\n');
console.log(`Wrote ${outPath} (${names.length} exports)`);
